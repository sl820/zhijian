# Create reverse map: variant -> standard character(s)
REVERSE_VARIANT_MAP: Dict[str, Set[str]] = defaultdict(set)
for standard, variants in VARIANT_CHAR_MAP.items():
    REVERSE_VARIANT_MAP[standard].add(standard)
    for variant in variants:
        REVERSE_VARIANT_MAP[variant].add(standard)

# Create taboo replacement reverse map: taboo_char -> list of (dynasty, emperor, replacement)
TABOO_REVERSE: Dict[str, List[Tuple[str, str, str]]] = defaultdict(list)
for dynasty, dynasties in TABOO_RULES.items():
    if not isinstance(dynasties, dict):
        continue
    for emperor_type, emperors in dynasties.items():
        if not isinstance(emperors, dict):
            continue
        for emperor, replacements in emperors.items():
            if not isinstance(replacements, dict):
                continue
            for taboo_char, replacement in replacements.items():
                TABOO_REVERSE[taboo_char].append((dynasty, emperor, replacement))


